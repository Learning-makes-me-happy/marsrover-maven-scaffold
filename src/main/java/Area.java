public class Area {
    private int areaX;
    private int areaY;

    public Area(int areaX, int areaY) {
        this.areaX = areaX;
        this.areaY = areaY;
    }

    public int getAreaX() {
        return areaX;
    }

    public void setAreaX(int areaX) {
        this.areaX = areaX;
    }

    public int getAreaY() {
        return areaY;
    }

    public void setAreaY(int areaY) {
        this.areaY = areaY;
    }


}
